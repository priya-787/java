// basic java
class dot4
{
 public static void main(String arg[])
 {
  int i,j,k,n=4;
  for(i=1;i<=4;i++)
   {
    for(k=1;k<i;k++)
     {
      System.out.println("_");
     }
    for(j=1;j<=n;j++)
     {
      System.out.println("*");
     }
    System.out.println("\n");
   }
  }
}