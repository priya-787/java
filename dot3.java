// basic java
class dot3
{
 public static void main(String args[])
  {
   int i,j;
   for(i=1;i<=4;i++)
   { 
    for (j=1;j<=4;j++)
    {
     if(i+j>=5)
      {
       System.out.print("*");
      }
     else
      {
       System.out.println();
      }
     System.out.println();
   }
  }
}
}